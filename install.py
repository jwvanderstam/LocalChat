# ---------------------------------------------------------------------------
# Minimum requirements
# ---------------------------------------------------------------------------

MIN_PYTHON = (3, 10)
PGVECTOR_IMAGE = "pgvector/pgvector:pg16"
POSTGRES_CONTAINER = "localchat-postgres"

# ---------------------------------------------------------------------------
# Terminal colours  (ANSI — enabled on Windows 10+ via os.system(""))
# ---------------------------------------------------------------------------

if sys.platform == "win32":
    os.system("")

G = "\033[92m"   # green
Y = "\033[93m"   # yellow
R = "\033[91m"   # red
C = "\033[96m"   # cyan
B = "\033[1m"    # bold
X = "\033[0m"    # reset

def _ok(msg: str)   -> None: print(f"  {G}ok{X}    {msg}")
def _warn(msg: str) -> None: print(f"  {Y}warn{X}  {msg}")
def _fail(msg: str) -> None: print(f"  {R}fail{X}  {msg}")
def _info(msg: str) -> None: print(f"  {C}    {X}  {msg}")
def _head(msg: str) -> None: print(f"\n{B}{C}{msg}{X}")
def _step(msg: str) -> None: print(f"\n{B}{msg}{X}")

# ---------------------------------------------------------------------------
# Prerequisite checks
# ---------------------------------------------------------------------------

def check_python() -> bool:
    v = sys.version_info
    label = f"Python {v.major}.{v.minor}.{v.micro}"
    if v >= MIN_PYTHON:
        _ok(label)
        return True
    _fail(f"{label}  --  {MIN_PYTHON[0]}.{MIN_PYTHON[1]}+ required")
    return False


def _run(cmd: list) -> tuple:
    """Run a subprocess. Returns (success: bool, stdout_first_line: str)."""
    try:
        r = subprocess.run(cmd, capture_output=True, text=True, timeout=10)
        first = (r.stdout.strip().splitlines() or [""])[0]
        return r.returncode == 0, first
    except (FileNotFoundError, subprocess.TimeoutExpired):
        return False, ""


def check_docker() -> bool:
    found, ver = _run(["docker", "--version"])
    if found:
        _ok(ver)
        return True
    _warn("Docker not found  --  PostgreSQL will need manual setup")
    _info("https://docs.docker.com/get-docker/")
    return False


def check_ollama() -> bool:
    # Try CLI
    found, ver = _run(["ollama", "--version"])
    if found:
        _ok(f"Ollama  ({ver})")
        return True
    # Try HTTP (running but not in PATH)
    try:
        with urllib.request.urlopen("http://localhost:11434/api/version", timeout=3) as r:
            data = json.loads(r.read())
            _ok(f"Ollama API  (v{data.get('version', '?')})")
            return True
    except Exception:
        pass
    _warn("Ollama not found  --  required for LLM and embeddings")
    _info("https://ollama.ai")
    return False

# ---------------------------------------------------------------------------
# Interactive prompts
# ---------------------------------------------------------------------------

def ask(prompt: str, default: str = "", hidden: bool = False) -> str:
    hint = f"[{default}] " if default else ""
    display = f"  {C}{prompt}{X} {hint}: "
    while True:
        val = (getpass.getpass(display) if hidden else input(display)).strip()
        if val:
            return val
        if default:
            return default
        print(f"  {R}Required.{X}")


def ask_bool(prompt: str, default: bool = True) -> bool:
    hint = "Y/n" if default else "y/N"
    val = input(f"  {C}{prompt}{X} [{hint}]: ").strip().lower()
    return default if not val else val in ("y", "yes")

