def write_env(cfg: dict) -> None:
    Path(".env").write_text(_ENV.format(**cfg), encoding="utf-8")
    _ok(f"Written  {Path('.env').resolve()}")


def collect_env(docker_ok: bool) -> dict:
    print()
    app_env = ask("Environment", "development")
    is_dev  = app_env.startswith("dev")

    secret_key = secrets.token_urlsafe(32)
    jwt_secret = secrets.token_urlsafe(32)
    _info("SECRET_KEY and JWT_SECRET_KEY generated automatically.")

    admin_password = ask("Admin password  (/api/auth/login)", hidden=True)
    host = ask("Server host", "localhost")
    port = ask("Server port", "5000")

    print()
    pg_host     = ask("PostgreSQL host", "localhost")
    pg_port     = ask("PostgreSQL port", "5432")
    pg_user     = ask("PostgreSQL user", "postgres")
    pg_password = ask("PostgreSQL password", hidden=True)
    pg_db       = ask("PostgreSQL database", "rag_db")

    print()
    ollama_url  = ask("Ollama URL",        "http://localhost:11434")
    chat_model  = ask("Chat model",        "llama3.1:latest")
    embed_model = ask("Embedding model",   "nomic-embed-text:latest")

    print()
    redis_enabled  = ask_bool("Enable Redis cache?", default=False)
    redis_host, redis_port_r, redis_password = "localhost", "6379", ""
    if redis_enabled:
        redis_host     = ask("Redis host", "localhost")
        redis_port_r   = ask("Redis port", "6379")
        redis_password = ask("Redis password  (blank = none)", "")

    return dict(
        app_env=app_env,
        debug="True" if is_dev else "False",
        log_level="DEBUG" if is_dev else "INFO",
        secret_key=secret_key,
        jwt_secret=jwt_secret,
        admin_password=admin_password,
        host=host,
        port=port,
        pg_host=pg_host,
        pg_port=pg_port,
        pg_user=pg_user,
        pg_password=pg_password,
        pg_db=pg_db,
        ollama_url=ollama_url,
        chat_model=chat_model,
        embed_model=embed_model,
        redis_enabled=str(redis_enabled),
        redis_host=redis_host,
        redis_port=redis_port_r,
        redis_password=redis_password,
    )

