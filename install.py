def _docker(cmd: list, timeout: int = 30) -> tuple:
    """Run a docker subcommand. Returns (success, stdout, stderr)."""
    try:
        r = subprocess.run(
            ["docker"] + cmd,
            capture_output=True, text=True, timeout=timeout,
        )
        return r.returncode == 0, r.stdout.strip(), r.stderr.strip()
    except FileNotFoundError:
        return False, "", "docker not found"
    except subprocess.TimeoutExpired:
        return False, "", f"timed out after {timeout}s"


def start_postgres(user: str, password: str, db: str, port: str) -> None:
    # Check if the container already exists (running OR stopped)
    ok, names, _ = _docker([
        "ps", "-a",
        "--filter", f"name={POSTGRES_CONTAINER}",
        "--format", "{{.Names}}",
    ])
    existing = [n for n in names.splitlines() if n == POSTGRES_CONTAINER]

    if existing:
        # Container exists — check if it is running
        ok, state, _ = _docker([
            "inspect", "--format", "{{.State.Status}}", POSTGRES_CONTAINER,
        ])
        if state == "running":
            _ok(f"Container '{POSTGRES_CONTAINER}' is already running.")
            return
        # Stopped — just start it
        _info(f"Container '{POSTGRES_CONTAINER}' exists but is stopped — starting it.")
        ok, _, err = _docker(["start", POSTGRES_CONTAINER])
        if ok:
            _ok(f"Container '{POSTGRES_CONTAINER}' started.")
        else:
            _fail(f"docker start failed: {err}")
        return

    # Pull image (already cached after first run — docker is smart about this)
    print(f"  Pulling {PGVECTOR_IMAGE} ...")
    subprocess.run(["docker", "pull", PGVECTOR_IMAGE], check=False)

    run_ok, _, err = _docker([
        "run", "-d",
        "--name", POSTGRES_CONTAINER,
        "-e", f"POSTGRES_USER={user}",
        "-e", f"POSTGRES_PASSWORD={password}",
        "-e", f"POSTGRES_DB={db}",
        "-p", f"{port}:5432",
        "--restart", "unless-stopped",
        PGVECTOR_IMAGE,
    ], timeout=60)

    if run_ok:
        _ok(f"Container '{POSTGRES_CONTAINER}' started on port {port}.")
        _info("PostgreSQL will be ready in a few seconds.")
        return

    # Diagnose common failure modes
    err_lower = err.lower()
    if "port is already allocated" in err_lower or "address already in use" in err_lower:
        _fail(f"Port {port} is already in use.")
        _info("A PostgreSQL instance may already be running on this machine.")
        _info(f"Change PG_PORT in .env or stop the process using port {port}.")
    elif "conflict" in err_lower and "name" in err_lower:
        _fail("Container name conflict (unexpected — please run: docker rm localchat-postgres)")
    else:
        _fail(f"docker run failed: {err or '(no error output)'}")

# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def create_dirs() -> None:
    for d in ("uploads", "logs"):
        Path(d).mkdir(exist_ok=True)
    Path("uploads/.gitkeep").touch(exist_ok=True)
    _ok("Directories: uploads/  logs/")


def install_deps() -> None:
    print("  Running pip install -r requirements.txt ...")
    r = subprocess.run(
        [sys.executable, "-m", "pip", "install", "-r", "requirements.txt"]
    )
    if r.returncode == 0:
        _ok("Dependencies installed.")
    else:
        _fail("pip install failed — run  pip install -r requirements.txt  manually.")


def load_env_file() -> dict:
    """Parse .env into a plain dict (no external deps needed)."""
    result: dict = {}
    try:
        for line in Path(".env").read_text(encoding="utf-8").splitlines():
            line = line.strip()
            if line and not line.startswith("#") and "=" in line:
                k, _, v = line.partition("=")
                result[k.strip()] = v.strip()
    except FileNotFoundError:
        pass
    return result


def pull_model(model: str) -> None:
    print(f"  Pulling {model} ...")
    subprocess.run(["ollama", "pull", model], check=False)

# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main() -> None:
    _head("=" * 54)
    _head("  LocalChat  --  environment setup")
    _head("=" * 54)

    # ── 1. Prerequisites ───────────────────────────────────
    _step("[1/3]  Checking prerequisites")
    py_ok     = check_python()
    docker_ok = check_docker()
    ollama_ok = check_ollama()

    if not py_ok:
        print(f"\n{R}Python {MIN_PYTHON[0]}.{MIN_PYTHON[1]}+ is required. Aborting.{X}\n")
        sys.exit(1)

    # ── 2. Configure .env ──────────────────────────────────
    _step("[2/3]  Configure environment  (.env)")
    env_path = Path(".env")
    if env_path.exists():
        if ask_bool(".env already exists — overwrite?", default=False):
            write_env(collect_env(docker_ok))
        else:
            _info("Keeping existing .env.")
    else:
        write_env(collect_env(docker_ok))

    # ── 3. Finalise ────────────────────────────────────────
    _step("[3/3]  Finalising")
    create_dirs()

    if ask_bool("Install Python dependencies?", default=True):
        install_deps()

    if docker_ok and ask_bool("Start PostgreSQL + pgvector via Docker?", default=True):
        cfg = load_env_file()
        start_postgres(
            cfg.get("PG_USER", "postgres"),
            cfg.get("PG_PASSWORD", ""),
            cfg.get("PG_DB", "rag_db"),
            cfg.get("PG_PORT", "5432"),
        )

    if ollama_ok:
        cfg = load_env_file()
        chat  = cfg.get("OLLAMA_DEFAULT_MODEL",   "llama3.1:latest")
        embed = cfg.get("OLLAMA_EMBEDDING_MODEL", "nomic-embed-text:latest")
        if ask_bool(f"Pull chat model  ({chat})?", default=False):
            pull_model(chat)
        if ask_bool(f"Pull embedding model  ({embed})?", default=False):
            pull_model(embed)

    # ── Done ───────────────────────────────────────────────
    _head("\nSetup complete.")
    print(f"\n  Start the app:  {B}python app.py{X}")
    print(f"  Open browser:   {B}http://localhost:5000{X}\n")


if __name__ == "__main__":
    main()
